    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String username = jtxtuser.getText() , pass = jPass.getText();
        if(username.equals("Admin") && pass.equals("admin1234")){
            Tab1 tab = new Tab1();
            JOptionPane.showMessageDialog(null, "You are successfully loged in.", "Access Granted!", JOptionPane.INFORMATION_MESSAGE);
            this.hide();
            tab.setVisible(true);
        }
        else{
            JOptionPane.showMessageDialog(null, "Incorrect username or password, try again.", "Access Denied!", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

