    private void jPassKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jPassKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode()==KeyEvent.VK_ENTER){
            if(jtxtuser.getText().equals("Admin") && jPass.getText().equals("admin1234")){
            Tab1 tab = new Tab1();
            JOptionPane.showMessageDialog(null, "You are successfully loged in.", "Access Granted!", JOptionPane.INFORMATION_MESSAGE);
            this.hide();
            tab.setVisible(true);
        }
        else{
            JOptionPane.showMessageDialog(null, "Incorrect Username or Password!" , "Access Denied!" , JOptionPane.INFORMATION_MESSAGE);
        }
        }
    }//GEN-LAST:event_jPassKeyPressed

